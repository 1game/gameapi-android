package com.playtomic.android;

public class PlayerCountry {
	private String name;
	private String code;
	
	public String getName()
	{
		return name;
	}
	
	public String getCode()
	{
		return code;
	}
	
	public void setName(String n)
	{
		name = n;
	}
	
	public void setCode(String c)
	{
		code = c;
	}
}
